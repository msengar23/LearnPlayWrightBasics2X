let age = "Mamta"; // this is a string literal
let isStudent = true; // this is a boolean literal
let pi = 3.14; // this is a numeric literal
let name = 'Mamta'; // this is also a string literal (using single quotes)
let nullValue = null; // this is a null literal, representing the intentional absence of any object value
let undefinedValue; // this is an undefined literal, representing a variable that has been declared but not assigned a value

// typeof operator to check the type of literals

console.log(typeof age);
console.log(typeof isStudent);
console.log(typeof pi);
console.log(typeof name);
console.log(typeof nullValue);
console.log(typeof undefinedValue);